from random import randint

class xt:

    def add(n1, n2):
        return n1 + n2

    def subtract(n1, n2):
        return n1 - n2

    def divide(n1, n2):
        if n2 != 0:
            return n1 / n2

    def multiply(n1, n2):
        return n1 * n2

    def rnd(n1, n2):
        return randint(n1, n2)

