#Adding two numbers using a function
def add(a,b):
    return a+b

#Subtracting two numbers using a function
def sub(a,b):
    return a-b

#Multiplying two numbers using a function
def mul(a,b):
    return a*b

#Dividing two numbers using a function
def div(a,b):
    return a/b

#Finding the remainder using a function
def rem(a,b):
    return a%b
