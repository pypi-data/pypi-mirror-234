def get_komaru(list_index):
    if list_index < abs(len(komaru_list_gif)):
        return komaru_list_gif[abs(list_index)]
    else:
        raise Exception(f"-     : {len(komaru_list_gif)}\n    ")


def print_random_komaru(size_in_symbols=250):
    if type(size_in_symbols) is not int:
        raise TypeError('Number must be int')
    if size_in_symbols < 3:
        raise ValueError('Size must be > 3')
    image = Image.open(requests.get(get_random_komaru_png(), stream=True).raw)
    width, height = size_in_symbols, int(size_in_symbols // 2.3)
    image = image.resize((width, height))

    for y in range(image.size[1]):
        for x in range(image.size[0]):
            print(color("", image.getpixel((x, y))), end='')
        print()
